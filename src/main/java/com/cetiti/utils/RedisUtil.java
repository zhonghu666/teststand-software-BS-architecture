    private static final String INCR_KEY = "-INCR";

    /**
     * 将地理元素放入放入缓存
     */
    public boolean geoSet(String Date, String key, double longitude, double latitude) {
        try {
            if (!hasKey(key + INCR_KEY)) {
                redisTemplate.opsForValue().set(key + INCR_KEY, 0);
            }
            long i = incr(key + INCR_KEY, 1);

            redisTemplate.opsForGeo().add(Date + "-" + key, new Point(longitude, latitude), i);
            return true;
        } catch (Exception e) {
            return false;
        }
    }

    /**
     * 将地理元素拿出
     */
    public List<Point> geoGet(String Date, String key) {
        List<Point> pointsList = new ArrayList<>();
        try {

//			pointsList = redisTemplate.opsForGeo().position(key);
            int aa = (int) redisTemplate.opsForValue().get(key + INCR_KEY);
            List members = new ArrayList<>();
            for (int i = 1; i <= aa; i++) {
                members.add(i);
            }
            pointsList = redisTemplate.opsForGeo().position(Date + "-" + key, members.toArray());
            return pointsList;
        } catch (Exception e) {
            return pointsList;
        }
    }


    public long timing(String operationKey) {
        ValueOperations<String, Object> ops = redisTemplate.opsForValue();
        Long currentTime = System.currentTimeMillis();
        Long startTime = (Long) ops.get(operationKey + ":start");
        if (startTime == null) {
            startTime = currentTime;
            ops.set(operationKey + ":start", startTime);
        }
        long elapsedTime = currentTime - startTime;
        ops.set(operationKey + ":elapsed", elapsedTime, 15, TimeUnit.MINUTES);
        return elapsedTime;
    }

    public long getElapsedTime(String operationKey) {
        Object o = redisTemplate.opsForValue().get(operationKey + ":elapsed");
        if (o == null) {
            return 0;
        }
        return Long.parseLong(o.toString());
    }

    public long deleteKeysByPattern(String pattern) {
        Set<String> keys = redisTemplate.keys(pattern);
        if (!keys.isEmpty()) {
            return redisTemplate.delete(keys);
        }
        return 0;
    }

